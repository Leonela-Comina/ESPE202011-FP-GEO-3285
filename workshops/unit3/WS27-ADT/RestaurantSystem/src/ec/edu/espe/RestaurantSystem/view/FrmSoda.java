    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        Soda soda;
        String name;
        String color;
        String taste;
        float netContent;
        float price;
        SodaController sodaController = new SodaController();
        
        name = txtName.getText();
        color = cmbColor.getSelectedItem().toString();
        taste = cmbTaste.getSelectedItem().toString();
       netContent = Float.parseFloat(txtNetContent.getText());
        price = Float.parseFloat(txtPrice.getText());
        
       txtName.setText(txtASodaInformation.getText());
        
        soda = new Soda(name, color, taste, netContent, price);
        
        sodaController.save(soda);
        
        JOptionPane.showMessageDialog(rootPane,soda.getName());
        
       
        
                 
    }//GEN-LAST:event_BtnSaveActionPerformed

